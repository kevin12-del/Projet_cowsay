// newcow.c
#include <stdio.h>

// Affiche une vache ASCII sans bulle de texte
void affiche_vache() {
    printf(" \\   ^__^\n");
    printf("  \\  (oo)\\_______\n");
    printf("     (__)\\       )\\/\n");
    printf("         ||----w |\n");
    printf("         ||     ||\n");
}

int main() {
    affiche_vache();
    return 0;
}